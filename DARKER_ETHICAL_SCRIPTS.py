def get_darker_roast(headline: str) -> str:
    """
    Get darker satirical roast (ethical boundaries maintained)
    
    Focus: System critique, not trauma exploitation
    """
    
    hl = headline.lower()
    
    # Determine category
    if any(word in hl for word in ['corporation', 'billionaire', 'oligarch', 'elite', 'power']):
        category = 'oligarchy'
    elif any(word in hl for word in ['tech', 'ai', 'data', 'privacy', 'surveillance', 'algorithm']):
        category = 'digital_control'
    elif any(word in hl for word in ['health', 'medical', 'pharma', 'insurance', 'hospital']):
        category = 'biomedical'
    elif any(word in hl for word in ['climate', 'environment', 'pollution', 'carbon']):
        category = 'environment'
    elif any(word in hl for word in ['police', 'prison', 'justice', 'court', 'law']):
        category = 'injustice'
    elif any(word in hl for word in ['media', 'news', 'propaganda', 'censorship']):
        category = 'propaganda'
    else:
        category = 'general_dark'
    
    templates = DARKER_ROASTS.get(category, DARKER_ROASTS['general_dark'])
    template = random.choice(templates)
    
    return template.format(headline=headline)

